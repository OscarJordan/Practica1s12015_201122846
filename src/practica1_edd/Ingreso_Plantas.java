    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
        String path = "";
        String nombre;
        int ataque;
        int defensa;
        String tipo;
        JFileChooser selec = new JFileChooser();
        int dec = selec.showOpenDialog(null);
        if (dec == JFileChooser.APPROVE_OPTION) {
        File file = selec.getSelectedFile();
        path = file.getPath();
        } 
        else 
        {
        JOptionPane.showMessageDialog(null, "Proceso cancelado!");
        }
        
        LlenarImagen(path);
        nombre = Txt_Nombre.getText();
        ataque = Integer.parseInt(Txt_At.getText());
        defensa = Integer.parseInt(Txt_Def.getText());
        tipo = (String)Combo_Tipo.getSelectedItem();
        
        Lista_Plantas nuevo = new Lista_Plantas();
        nuevo.InsertarPlanta(path, nombre, defensa, ataque, tipo);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

