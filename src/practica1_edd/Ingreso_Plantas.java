    public static void TextoPlantas() throws FileNotFoundException
    {
        
        Lista_Plantas auxiliar = null;
        Objeto_Planta nuevo = null;
        nuevo = auxiliar.cabeza; 
        //JOptionPane.showMessageDialog(null, "Nombre: " + nuevo.nombre);
        while(nuevo != null)
        {
        if(nuevo.sig != null)
        {    
        cadenaplantas = cadenaplantas + nuevo.nombre + "->";
        //JOptionPane.showMessageDialog(null, cadenaplantas);
        nuevo = nuevo.sig;
        }
        else
        {
            cadenaplantas = cadenaplantas + nuevo.nombre + ";";  
            //JOptionPane.showMessageDialog(null, cadenaplantas);
            nuevo = nuevo.sig;
        }
        }
     
        //JOptionPane.showMessageDialog(null, "Termino bien la cadena de plantas");
    }
      
    
    

